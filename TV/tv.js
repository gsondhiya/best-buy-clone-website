  function sortItems(){
  var sorting=document.querySelector("#sort").value
  // console.log(sorting)
  
  if(sorting=="h2l"){
    tvData.sort(function(a,b){
      return b.price-a.price
    })
  // console.log(sorting)
    display(tvData)
  }
  else if(sorting=="l2h"){
    tvData.sort(function(a,b){
      return a.price-b.price
    })
    display(tvData)
  }
  else if (sorting == "acending") {
    tvData.sort(function (a, b) {
      if(a.name>b.name){
        return 1
      }
      if(a.name<b.name){
        return -1
      }
    });
    display(tvData);
  }
  else if (sorting == "decending") {
    tvData.sort(function (a,b) {
      if(b.name>a.name){
        return 1
      }
      if(b.name<a.name){
        return -1
      }
    });
    display(tvData);
  }
}
